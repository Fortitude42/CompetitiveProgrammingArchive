void dfs(int v, int p = 0) {
	cnt[v] = (s[v] == 'S');
	for (auto i : g[v]) {
		int to = a[i] ^ b[i] ^ v;
		if (to == p)
			continue;

		dfs(to, v);

		if (to == b[i])
			x[to] = -1;
		cnt[v] += cnt[to];
	}

	x[v] += cnt[v];
}

vector < int > gg[N];


void dfs1(int v, int p = 0) {
	gg[v].resize(2);
}
 
