int n, m;
ll dp[N];
ll f[(ll)3000 + 123], fa[3333], rv[3333];

ll bp(ll x, ll y){
	ll z = 1;
	while(y){
		if(y & 1)
			z = z * x % mod;
		x = x * x % mod;
		y >>= 1;
	}
	return z;
}

ll cnk(ll x, ll y){
	if(y < 0 || x < y)
		return 0;
	return f[x] * rv[y] % mod * rv[x - y] % mod;
}

ll ank(ll x, ll y){
	if(y < 0 || x < y)
		return 0;
	return f[x] * rv[x - y] % mod;
}

ll aank(ll x, ll y){
	if(y < 0 || x < y)
		return 0;
	return fa[m - x] * bp(fa[m - x + y], mod - 2) % mod;
}

int main(){
	cin >> n >> m;
	f[0] = 1;
	rv[0] = 1;
	for(int i = 1; i <= 3002; ++i){
		f[i] = f[i - 1] * i % mod;
		rv[i] = bp(f[i], mod - 2);
	}
	if(m <= 3002){	
		dp[0] = 1;
		for(int i = 1; i <= n; ++i){
			dp[i] = dp[i - 1];
			for(int j = 0; j < i; ++j){
				dp[i] += dp[j] * ank(m - 2, i - j - 1) % mod * cnk(n - j - 1, i - j - 1) % mod;
				dp[i] %= mod;
			}
		}
		cout << dp[n];
		return 0;
	}
	fa[0] = F[m / 1000000];
	for(int i = m - m % 1000000 + 1; i <= m; ++i)
		fa[0] = fa[0] * i % mod;
	for(int i = 1; i <= 3002; ++i){
        fa[i] = fa[i - 1] * bp(m - i + 1, mod - 2) % mod;
		if(m - i == 0)
			fa[i] = 1;
	}
	dp[0] = 1;
	for(int i = 1; i <= n; ++i){
		dp[i] = dp[i - 1];
		for(int j = 0; j < i; ++j){
			dp[i] += dp[j] * aank(m - 2, i - j - 1) % mod * cnk(n - j - 1, i - j - 1) % mod;
			dp[i] %= mod;
			if (i == 5)
				cerr << aank(m - 2, i - j - 1) << ' ' << cnk(n - j - 1, i - j - 1) << endl;
		}
	}
	cerr << dp[5] << endl;
	cout << dp[n];
	return 0;
}